package com.learn.day001;

public class Demo006 {
    /*
    * Java中的数组
    *   Java中的数组是固定长度的数组，
    * */
    public static void main(String[] args) {
        //定义固定长度的数组：
        int arr[] = new int[10];
        arr[0] = 9;
        System.out.println(arr[0]);

        int arr1[] = {1,2,4,5};
        System.out.println(arr1[0]);
    }
}
