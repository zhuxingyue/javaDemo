package com.learn.day004;

public class GuanXiYunSuanFu {
    public static void main(String[] args) {
        // ==   !=  >   >=  <   <=
        // 关系运算结果就两种true，false
        int a = 10;
        int b = 20;
        short c = 10;

        System.out.println(a==c);
        System.out.println(a!=c);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);



    }
}
